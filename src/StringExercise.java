public class StringExercise {
    public static void main(String[] args) {
        String pinkFloyd = "We don't need no education %nWe don't need no thought control%n";
        System.out.printf(pinkFloyd);
        String quotes = "Check \"this\" out!, \"s inside of \"s!%n";
        System.out.printf(quotes);
        String mainDrive = "In windows, the main drive is usually C:\\%n";
        System.out.printf(mainDrive);
        String backslash = """
                I can do backslashes \\, double backslashes \\\\,
                and the amazing triple backslash \\\\\\!""";
        System.out.printf(backslash);
    }
}
